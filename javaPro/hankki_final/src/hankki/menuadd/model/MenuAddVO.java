package hankki.menuadd.model;

public class MenuAddVO {
	private int num;
	private String kind;
	private String name;
	private int cooktm;
	private int price;

	public MenuAddVO() {
	}
	
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
	
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	public int getCooktm() {
		return cooktm;
	}
	public void setCooktm(int cooktm) {
		this.cooktm = cooktm;
	}
	
	
	
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}

}