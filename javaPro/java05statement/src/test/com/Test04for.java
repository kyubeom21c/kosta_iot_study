package test.com;

public class Test04for {

	public static void main(String[] args) {
		// 반복문for : 카운팅,길이,용량
		System.out.println("for...");
		
		for (int a = 0; a<10  ;a++) {
			System.out.println("for..."+a);
		}
		
		for (int i = 0; i < 100; i++) {
			System.out.println("i..."+i);
		}
		
		for (char i = 'A'; i <= 'z'; i++) {
			System.out.println(i+"======"+(int)i);
		}
	}

}
